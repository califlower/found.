package com.example.cal13.found;

public class User {

    public String name;
    public String email;
    public String number;
    public String location;
    public String distance;

    public User() {

    }

    public User(String name, String email, String number, String location, String distance)
    {
        this.name = name;
        this.email = email;
        this.number=number;
        this.location=location;
        this.distance=distance;
    }

}

